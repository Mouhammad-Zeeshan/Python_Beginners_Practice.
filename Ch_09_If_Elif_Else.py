### HERE WE DISCUSSS THREE CONDITIONAL STATEMENT. 
# 01 - IF
# 02 - ELIF         (We can use more than 1 ELIF condition).
# 03 - ELSE
# Let's discuss a scenario using the above operation.


x = 1

min_age_for_admission = 5

if x == min_age_for_admission:
    print("Congratulations! you can get admission")
elif x > min_age_for_admission:
    print("You should go for middle school")
else:
    print("Your children is not eligible to get admission")

